import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const packageRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const packageJson = JSON.parse(readFileSync(resolve(packageRoot, "package.json"), "utf-8"));
const packageNamePrefix = "@aoagents/ao-plugin-agent-";
const pluginName = packageJson.name.startsWith(packageNamePrefix)
  ? packageJson.name.slice(packageNamePrefix.length)
  : packageJson.name;

const contents = `// Generated by scripts/generate-manifest.mjs. Do not edit by hand.\nexport const pluginName = ${JSON.stringify(pluginName)};\nexport const pluginDescription = ${JSON.stringify(packageJson.description)};\nexport const pluginVersion = ${JSON.stringify(packageJson.version)};\n`;

writeFileSync(resolve(packageRoot, "src/generated-manifest.ts"), contents, "utf-8");
